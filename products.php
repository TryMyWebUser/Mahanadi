                    link.addEventListener('click', function (e) {
                        const targetId = this.getAttribute('href');
                        const target = document.querySelector(targetId);

                        if (target) {
                            setTimeout(() => {
                                const navbar = document.querySelector('.navbar'); // change selector if needed
                                const navbarHeight = navbar ? navbar.offsetHeight : 0;

                                const y = target.getBoundingClientRect().top + window.pageYOffset - navbarHeight - 200;
                                window.scrollTo({ top: y, behavior: 'smooth' });
                            }, 200); // delay ensures tab content is visible before scroll
                        }
                    });
                });
            });
